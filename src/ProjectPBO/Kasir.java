    private void ShowData(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn ("Nama barang");
        model.addColumn ("Harga barang");
        model.addColumn ("Jumlah");
        model.addColumn ("Total harga");
        try{
            String sql = "SELECT barang.barang_nama, detail_transaksi.dtransaksi_harga, detail_transaksi.dtransaksi_jumlah, (detail_transaksi.dtransaksi_jumlah*detail_transaksi.dtransaksi_harga) AS 'Total harga'"
                    + "FROM detail_transaksi INNER JOIN barang ON barang.barang_id = detail_transaksi.barang_id WHERE transaksi_id = "+transaksiid+";";
            java.sql.Connection conn;
            conn = (Connection)Config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),"Rp."+res.getString(2), res.getString(3),"Rp."+res.getString(4)});
                
            }
            tabel.setModel(model);
            
        }catch(SQLException e){
            System.out.println("Error "+ e.getMessage());
        }
        
    }
    
    
    
