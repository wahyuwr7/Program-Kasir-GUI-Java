    private void txtnamabarangMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtnamabarangMouseEntered
        txtnamabarang.setText(Sesi.get_namabarang());
        String sql = "SELECT * FROM barang WHERE barang_nama = '"+txtnamabarang.getText()+"';";
        int temp;
        try{
            java.sql.Connection conn = (Connection)Config.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            Statement stm = (Statement)conn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while(rs.next()){
                temp = Integer.parseInt(rs.getString(5)) + Integer.parseInt(rs.getString(5))*10/100;
                harga = Integer.toString(temp) ;
                barangid = rs.getString(1);
                txtharga.setText("Rp."+harga);
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_txtnamabarangMouseEntered

