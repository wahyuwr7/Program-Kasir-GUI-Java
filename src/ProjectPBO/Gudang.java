    private void btcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcariActionPerformed
        String kode = txtkode.getText();
        String nama = txtnama.getText();

        if("".equals(kode) && "".equals(nama)){
            tampilkanbarang("SELECT * FROM barang");
        } else if("".equals(nama) && !"".equals(kode)){
            try{
                String sql = "SELECT * FROM barang WHERE barang_id = "+kode+";";
                java.sql.Connection conn = (Connection)Config.configDB();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                tampilkanbarang(sql);
            }catch(HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else if(!"".equals(nama) && "".equals(kode)){
            try{
                String sql = "SELECT * FROM barang WHERE barang_nama = '"+nama+"';";
                java.sql.Connection conn = (Connection)Config.configDB();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                tampilkanbarang(sql);
            }catch(HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }else{
            try{
                String sql = "SELECT * FROM barang WHERE barang_id = "+kode+" OR barang_nama = '"+nama+"';";
                java.sql.Connection conn = (Connection)Config.configDB();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                tampilkanbarang(sql);
            }catch(HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }

    }//GEN-LAST:event_btcariActionPerformed

