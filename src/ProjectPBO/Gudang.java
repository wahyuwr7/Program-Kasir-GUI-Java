    private void tampilkanbarang(String query){
         DefaultTableModel model = new DefaultTableModel();
        model.addColumn ("Barcode");
        model.addColumn ("Nama Barang");
        model.addColumn ("Merk");
        model.addColumn ("Stok");
        model.addColumn ("Harga");
        model.addColumn ("Expired");
        try{
            String sql = query;
            java.sql.Connection conn;
            conn = (Connection)Config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2), res.getString(3), res.getString(4)+" pcs","Rp."+ res.getString(5), res.getString(6)});
                
            }
            tabelbarang.setModel(model);
            
        }catch(SQLException e){
            System.out.println("Error "+ e.getMessage());
        }
    }
    
    private void admin(){
        String id = Sesi.get_id();
        String sql ="SELECT * FROM pegawai WHERE user_id= "+id+";";
        
        try{
            java.sql.Connection conn = (Connection)Config.configDB();
            java.sql.PreparedStatement pstm;
            pstm = conn.prepareStatement(sql);
            ResultSet rs = pstm.executeQuery();
            
            while(rs.next()){
                jadmin.setText(rs.getString("pegawai_nama"));
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
